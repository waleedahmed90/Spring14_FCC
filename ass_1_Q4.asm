;;;USING DO_WHILE LOOP

.MODEL SMALL
.STACK 100H
.DATA
M1 DB 'UPPER CASE: $'
M2 DB 'LOWER CASE: $'
M3 DB 'DIGITS:     $'
.CODE
MAIN PROC 
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,9
	LEA DX,M1
	INT 21H
	
	MOV AH,2
	MOV DL,41H
	MOV CX,26
	
	
	;;;;UPPER CASE LETTERS
@_LOOP1:
		INT 21H
		INC DL
		DEC CX
		JNZ @_LOOP1

		MOV DL,0DH
		INT 21H
		MOV DL,0AH
		INT 21H
		
		LEA DX,M2
		MOV AH,9
		INT 21H
		
	;;;;;LOWER CASE LETTERS	
	MOV AH,2
	MOV DL,61H
	MOV CX,26
	
@_LOOP2:
		INT 21H
		INC DL
		DEC CX
		JNZ @_LOOP2
		
		MOV DL,0DH
		INT 21H
		MOV DL,0AH
		INT 21H
		;;;DIGITS
		LEA DX,M3
		MOV AH,9
		INT 21H
		
		MOV AH,2
		MOV DL,30H
		MOV CX,10

@_LOOP3:
		INT 21H
		INC DL
		DEC CX
		JNZ @_LOOP3
		
		
;;;RETURNING CONTROL TO DOS		
MOV AH,4CH
INT 21H		


		MAIN ENDP
END MAIN