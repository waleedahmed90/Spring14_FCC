;;;MOVING CURSOR TO THE NEXT LINE
.MODEL SMALL
.STACK 100H
.DATA
M1 DB 'FIRST MESSAGE$'
M2 DB 'MESSAGE AFTER MOVING CURSOR TO THE NEXT LINE$'
.CODE

MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,9
	LEA DX,M1
	INT 21H

CALL NEXTLINE
	
	MOV AH,9
	LEA DX,M2
	INT 21H
	
	
	;;;RETURNING CONTROL TO DOS
	
	MOV AH,4CH
	INT 21H



NEXTLINE PROC	
	;;MOVING CURSOR TO THE NEXT LINE
	MOV AH,2	;SINGLE CHAR OUTPUT MODE
	MOV DL,0DH	;CARRIAGE RETURN
	INT 21H		;;SHOOT;;
	MOV DL,0AH	;LINE FEED
	INT 21H		;;SHOOT;;
RET
NEXTLINE ENDP	
	
	
	MAIN ENDP
END MAIN
	