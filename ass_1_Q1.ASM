.MODEL SMALL
.STACK 100H
.DATA

M1 DB 'ENTER THE FIRST DIGIT:$'
M2 DB 'ENTER THE 2ND DIGIT:$'
M3 DB 'SUM:$'
D1 DB ?
D2 DB ?
SUM DB ?

.CODE
		MAIN PROC 
			MOV AX, @DATA
			MOV DS, AX
			;DISPLAYING THE M1
			MOV AH,9
			LEA DX,M1
			INT 21H
			;TAKING INPUT---->DIGIT-1
			MOV AH,1
			INT 21H
			MOV D1,AL
			;NEXT LINE
			MOV AH,2
			MOV DL,0AH
			INT 21H
			MOV DL,0DH
			INT 21H
			;DISPLAYING THE M2
			MOV AH,9
			LEA DX,M2
			INT 21H
			;TAKING INPUT---->DIGIT-2
			MOV AH,1
			INT 21H
			MOV D2,AL
			;NEXT LINE
			MOV AH,2
			MOV DL,0AH
			INT 21H
			MOV DL,0DH
			INT 21H

			;DIPLAYING THE M3
			MOV AH,9
			LEA DX,M3
			INT 21H
			
			
			;CALCULATING THE SUM	

			MOV CH,D1
			ADD CH,D2
			MOV SUM,CH
			SUB SUM,30H
			;DISPLAYING THE OUTPUT OF SUM
			MOV AH,2
			MOV DL,SUM
			INT 21H


;RETURNING CONTROL TO DOS
MOV AH,4CH
INT 21H

		MAIN ENDP
END MAIN