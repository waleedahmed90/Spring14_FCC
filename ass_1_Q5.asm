;;;USING WHILE LOOP

.MODEL SMALL
.STACK 100H
.DATA
M1 DB 'UPPER CASE: $'
M2 DB 'LOWER CASE: $'
M3 DB 'DIGITS:     $'
.CODE
MAIN PROC 
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,9
	LEA DX,M1
	INT 21H
	
	MOV AH,2
	MOV DL,41H
	MOV CX,26
	
	
	;;;;UPPER CASE LETTERS
@_WHILE_LOOP1:
		CMP CX,0
		JE @_END_WHILE1
		INT 21H
		INC DL
		DEC CX
		JMP @_WHILE_LOOP1

@_END_WHILE1:
		
		MOV DL,0DH
		INT 21H
		MOV DL,0AH
		INT 21H
		
		LEA DX,M2
		MOV AH,9
		INT 21H
		
	;;;;;LOWER CASE LETTERS	
	MOV AH,2
	MOV DL,61H
	MOV CX,26
	
@_WHILE_LOOP2:
		CMP CX,0
		JE @_END_WHILE2
		INT 21H
		INC DL
		DEC CX
		JMP @_WHILE_LOOP2

@_END_WHILE2:
		
		MOV DL,0DH
		INT 21H
		MOV DL,0AH
		INT 21H
		;;;DIGITS
		LEA DX,M3
		MOV AH,9
		INT 21H
		
		MOV AH,2
		MOV DL,30H
		MOV CX,10

@_WHILE_LOOP3:
		CMP CX,0
		JE @_END_WHILE3
		INT 21H
		INC DL
		DEC CX
		JMP @_WHILE_LOOP3
		
@_END_WHILE3:		
		
		
;;;RETURNING CONTROL TO DOS		
MOV AH,4CH
INT 21H		


		MAIN ENDP
END MAIN