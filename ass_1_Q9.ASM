;;;binary output

.MODEL SMALL
.STACK 100H
.CODE
MAIN PROC
   MOV BX, 1010110001010011B

CALL BINARYOUTPUT

	MOV AH, 4CH
	INT 21H
MAIN ENDP

BINARYOUTPUT PROC

   MOV AH, 2
  MOV CX, 16
 @FOR:
     ROL BX, 1
    JNC @DISPLAY_ZERO
        ; Display 1
    MOV DL, 31H
    INT 21H
   JMP @ENDFOR

   @DISPLAY_ZERO:
        MOV DL, 30H
        INT 21H

@ENDFOR:
LOOP @FOR

RET 
BINARYOUT ENDP
END MAIN
