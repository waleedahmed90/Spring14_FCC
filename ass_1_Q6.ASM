.MODEL SMALL
.STACK 100H

.DATA
M1 DB ' IS AN UPPER CASE LETTER$'
M2 DB ' IS A LOWER CASE LETTER$'
M3 DB ' IS A DIGIT$'
M4 DB ' IS NOT AN ALPHANUMERIC CHARACTER$'
TEMP DB ?

.CODE
MAIN PROC
	
		MOV AX,@DATA
		MOV DS,AX

		MOV AH,2
		MOV DL,'?'
		INT 21H
		
		MOV AH,1
		INT 21H
		MOV TEMP,AL
		
		MOV AH,2
		MOV DL,0DH
		INT 21H
		MOV DL,0AH
		INT 21H
		
		
		CMP TEMP,30H
		JGE @_DIGIT
@_BACK1:		
		CMP TEMP,41H
		JGE @_UPPER
@_BACK2:
		CMP TEMP,61H
		JGE @_LOWER
@_BACK3:
		;;HERE GOES THE LAST MESSAGE
		MOV AH,2
		MOV DL,TEMP
		INT 21H
		LEA DX,M4
		MOV AH,9
		INT 21H
		JMP @_EXIT
		
@_DIGIT:
	CMP TEMP,39H
	JGE @_BACK1
	MOV AH,2
	MOV DL,TEMP
	INT 21H
	LEA DX,M3
	MOV AH,9
	INT 21H
	JMP @_EXIT
	
@_UPPER:
	CMP TEMP,5AH
	JGE @_BACK2
	MOV AH,2
	MOV DL,TEMP
	INT 21H
	LEA DX,M1
	MOV AH,9
	INT 21H
	JMP @_EXIT	
	
@_LOWER:
	CMP TEMP,7AH
	JGE @_BACK3
	MOV AH,2
	MOV DL,TEMP
	INT 21H
	LEA DX,M2
	MOV AH,9
	INT 21H
	JMP @_EXIT	
	

@_EXIT:
;;;;CONTROL----->DOS
	MOV AH,4CH
	INT 21H
	
MAIN ENDP
END MAIN	